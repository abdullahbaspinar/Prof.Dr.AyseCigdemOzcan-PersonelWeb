// ===== Language Switching =====
function switchLanguage(lang) {
    currentLang = lang;
    document.documentElement.lang = lang;
    
    // Store original HTML content for all elements with data-i18n before first translation
    // This allows HTML content to be preserved and restored when needed
    document.querySelectorAll('[data-i18n]').forEach(element => {
        if (!element.dataset.originalContent) {
            // Store original HTML content (either textContent or innerHTML depending on element type)
            if (element.tagName === 'UL' || element.tagName === 'OL' || element.tagName === 'P' || element.tagName === 'DIV') {
                element.dataset.originalContent = element.innerHTML;
            } else {
                element.dataset.originalContent = element.textContent || element.innerHTML;
            }
        }
    });
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        
        // Check if element has original HTML content that should be preserved
        const hasOriginalContent = element.dataset.originalContent && element.dataset.originalContent.trim() !== '';
        
        // Apply translation only if available and user explicitly wants translation
        // Otherwise, always use HTML content (which is the source of truth)
        if (translations[lang] && translations[lang][key]) {
            // Special handling for email links
            if (element.tagName === 'A' && (key === 'contact.email' || element.id === 'contactEmailLink')) {
                const email = translations[lang]['contact.email'];
                element.textContent = email;
                element.href = 'mailto:' + email;
            } 
            // Special handling for education list items with labels
            else if (element.tagName === 'LI' && (key.startsWith('cv.education.highSchool') || key.startsWith('cv.education.bachelor') || key.startsWith('cv.education.master') || key.startsWith('cv.education.phd') || key.startsWith('cv.education.associate') || key.startsWith('cv.education.professor'))) {
                const translation = translations[lang][key];
                const parts = translation.split(':');
                if (parts.length > 1) {
                    element.innerHTML = '<span class="cv-label">' + parts[0] + ':</span> ' + parts.slice(1).join(':');
                } else {
                    element.innerHTML = translation;
                }
            }
            // For lists (UL, OL), only apply translation if it exists and is not empty
            // Otherwise keep HTML content
            else if (element.tagName === 'UL' || element.tagName === 'OL') {
                const translation = translations[lang][key];
                if (translation && translation.trim() !== '') {
                    element.innerHTML = translation;
                } else if (hasOriginalContent) {
                    // Restore original HTML content if translation is empty
                    element.innerHTML = element.dataset.originalContent;
                }
            }
            // For other elements, apply translation
            else if (element.tagName === 'STRONG') {
                element.textContent = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        } else if (hasOriginalContent) {
            // No translation available, restore original HTML content
            if (element.tagName === 'UL' || element.tagName === 'OL' || element.tagName === 'P' || element.tagName === 'DIV') {
                element.innerHTML = element.dataset.originalContent;
            } else {
                element.textContent = element.dataset.originalContent;
            }
        }
        // If no original content stored and no translation, HTML content (already in DOM) stays as is
    });
    
    // Update language toggle buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
            btn.setAttribute('aria-pressed', 'true');
        } else {
            btn.classList.remove('active');
            btn.setAttribute('aria-pressed', 'false');
        }
    });
    
    // Store language preference
    localStorage.setItem('preferredLanguage', lang);
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
    // First, store all original HTML content before any translation
    // This ensures HTML content is always preserved and can be restored
    document.querySelectorAll('[data-i18n]').forEach(element => {
        // Always store current HTML content as the source of truth
        if (element.tagName === 'UL' || element.tagName === 'OL' || element.tagName === 'P' || element.tagName === 'DIV' || element.tagName === 'SPAN') {
            element.dataset.originalContent = element.innerHTML;
        } else {
            element.dataset.originalContent = element.textContent || element.innerHTML;
        }
    });
    
    // Check for saved language preference or default to English
    const savedLang = localStorage.getItem('preferredLanguage') || 'en';
    
    // Only apply translation if it's not the default language or if user explicitly changed language
    // For default language (en), keep HTML content as is
    if (savedLang !== 'en') {
        switchLanguage(savedLang);
    }
    // For English, HTML content stays as is (already in DOM)
    
    // Language toggle button event listeners
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', function() {
            const lang = this.getAttribute('data-lang');
            switchLanguage(lang);
        });
    });
});

