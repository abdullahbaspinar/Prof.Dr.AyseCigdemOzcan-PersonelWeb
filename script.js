const contactForm = document.getElementById('contactForm');

function showError(fieldId, errorId, message) {
    const field = document.getElementById(fieldId);
    const errorElement = document.getElementById(errorId);
    if (field && errorElement) {
        field.setAttribute('aria-invalid', 'true');
        errorElement.textContent = message;
        errorElement.classList.add('show');
    }
}

function clearError(fieldId, errorId) {
    const field = document.getElementById(fieldId);
    const errorElement = document.getElementById(errorId);
    if (field && errorElement) {
        field.setAttribute('aria-invalid', 'false');
        errorElement.textContent = '';
        errorElement.classList.remove('show');
    }
}

// Dynamic form validation based on HTML structure
function validateForm() {
    let isValid = true;
    
    // Get all required fields dynamically from the form
    const requiredFields = contactForm.querySelectorAll('[required]');
    
    requiredFields.forEach(field => {
        const fieldId = field.id;
        const errorId = fieldId + 'Error';
        const value = field.value.trim();
        const fieldType = field.type;
        const fieldName = field.name;
        
        // Clear previous error
        clearError(fieldId, errorId);
        
        // Check if field is empty
        if (!value) {
            const errorKey = `form.error.${fieldName || fieldId}`;
            const errorMessage = translations[currentLang] && translations[currentLang][errorKey] 
                ? translations[currentLang][errorKey]
                : `${field.getAttribute('data-i18n') ? translations[currentLang][field.getAttribute('data-i18n')] || field.name : field.name} is required`;
            showError(fieldId, errorId, errorMessage);
            isValid = false;
            return;
        }
        
        // Email validation
        if (fieldType === 'email') {
            const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
            if (!emailRegex.test(value)) {
                showError(fieldId, errorId, translations[currentLang]['form.error.emailInvalid'] || 'Please enter a valid email address');
                isValid = false;
            }
        }
        
        // Message/textarea validation (minimum length)
        if (field.tagName === 'TEXTAREA' && value.length < 10) {
            showError(fieldId, errorId, translations[currentLang]['form.error.messageShort'] || 'Message must be at least 10 characters');
            isValid = false;
        }
    });
    
    return isValid;
}

if (contactForm) {
    // Real-time validation - dynamically get all form fields
    const formFields = contactForm.querySelectorAll('input, textarea');
    formFields.forEach(field => {
        const fieldId = field.id;
        if (fieldId) {
            field.addEventListener('blur', function() {
                validateForm();
            });
            field.addEventListener('input', function() {
                const errorId = fieldId + 'Error';
                clearError(fieldId, errorId);
            });
        }
    });

    contactForm.addEventListener('submit', function(e) {
        e.preventDefault();
        
        if (!validateForm()) {
            // Focus on first error field
            const firstError = contactForm.querySelector('[aria-invalid="true"]');
            if (firstError) {
                firstError.focus();
            }
            return;
        }
        
        // Get form values dynamically
        const formData = new FormData(contactForm);
        const formValues = {};
        formData.forEach((value, key) => {
            formValues[key] = value.trim();
        });
        
        // Build email body dynamically from all form fields
        let emailBody = '';
        formFields.forEach(field => {
            if (field.name && formValues[field.name]) {
                const label = field.previousElementSibling?.textContent || field.name;
                emailBody += `${label}: ${formValues[field.name]}\n`;
            }
        });
        
        // Prepare email content
        const recipientEmail = document.querySelector('#contactEmailLink')?.href?.replace('mailto:', '') || 'ozcan@hacettepe.edu.tr';
        const name = `${formValues.firstName || ''} ${formValues.lastName || ''}`.trim() || 'Contact Form Submission';
        const subject = encodeURIComponent(`Contact Form - ${name}`);
        const body = encodeURIComponent(emailBody);
        
        // Create mailto link
        const mailtoLink = `mailto:${recipientEmail}?subject=${subject}&body=${body}`;
        
        // Open default mail application
        try {
            window.location.href = mailtoLink;
            
            // Reset form after a short delay
            setTimeout(function() {
                contactForm.reset();
                // Clear all errors dynamically
                formFields.forEach(field => {
                    if (field.id) {
                        clearError(field.id, field.id + 'Error');
                    }
                });
            }, 500);
        } catch (error) {
            console.error('Error opening mail client:', error);
            alert(translations[currentLang]['form.error.mailClient'] || 'Unable to open mail client. Please send an email manually to ' + recipientEmail);
        }
    });
}

